

/*******************************************************
    Metodos HTTP:
        GET: Buscar informações do Back-end
        POST: Incluir informações no Back-end 
        PUT: Alterar informações no Back-end 
        DELETE: Deletar informações no Back-end
    -------------------------------------------
    Tipos de parametro:
        Query Params: São parametros nomeados nas rotas para 
            busca que é sinalizado por uma "?" seguido 
            do nome do parametro e seu valor, 
            Ex: /user?nome=jonas, utilizados para 
            filtro e paginação
        Route Params: Parametros utilizados para indentificar recursos,
            ele é utilizado diretamente na rota, para definir o parametro
        Request body: Corpo da requisição, utilizado para criar ou alterar recursos 
*******************************************************/
